package vo;

public class JournalIssue {
	private String issNum;
	private String scope;
	private String issEditor;

	public String getIssNum() {
		return issNum;
	}

	public void setIssNum(String issNum) {
		this.issNum = issNum;
	}

	public String getScope() {
		return scope;
	}

	public void setScope(String scope) {
		this.scope = scope;
	}

	public String getIssEditor() {
		return issEditor;
	}

	public void setIssEditor(String issEditor) {
		this.issEditor = issEditor;
	}
}
